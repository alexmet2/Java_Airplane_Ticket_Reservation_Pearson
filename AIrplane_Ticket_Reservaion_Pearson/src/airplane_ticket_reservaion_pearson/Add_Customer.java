            JFileChooser picchooser = new JFileChooser(); /* declares picchooser as a variable */
            picchooser.showOpenDialog(null); /* It is a part of the JFileChooser */
            File pic = picchooser.getSelectedFile(); 

            FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images","png","jpg"); /* declares filter as a variable and gives us images formats */
