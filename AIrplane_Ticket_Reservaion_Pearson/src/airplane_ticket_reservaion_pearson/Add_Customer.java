    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        String Customer_ID = Customer_ID_Add_Customer.getText(); /* it gets the text that the user has input on the text Field  */
        String First_Name = First_Name_Add_Customer.getText();
        String Last_Name = Last_Name_Add_Customer.getText();
        String Passport_ID = Passport_ID_Add_Customer.getText();
        String Address = Address_Add_Customer.getText();
        
        DateFormat da = new SimpleDateFormat("yyyy-MM-dd"); /* it declares the format and gets the date of birth */
        String date = da.format(DOB_Add_Customer.getDate()); /* convertion of the date variable as a String */
        
        String Gender;
        
        if (GM_Add_Customer.isSelected()){ /* gets the selected item between male/female */
            Gender = "Male";
        }
        else{
            Gender = "Female";
        }
        
        String Contact = Contact_Add_Customer.getText();
        
        if(Customer_ID.isEmpty() || First_Name.isEmpty() || Last_Name.isEmpty() ||  Passport_ID.isEmpty() || Address.isEmpty() || date.isEmpty() || Contact.isEmpty() || userimage == null){
            JOptionPane.showMessageDialog(this, "Some of the fields may be blank...Please fill them out...");
        } /* shows a message to fill the empty fields if they are not filled by the user */
        else{
            try {
                Class.forName("com.mysql.jdbc.Driver"); /* it is a method that returns the class object associated with the class or interface passed as the first parameter */
                con = DriverManager.getConnection("jdbc:mysql://localhost/airplane_ticket_reservation","root",""); /* establishes connection to the local host */
                pst = con.prepareStatement("insert into customer(ID,First_Name,Last_Name,Passport_ID,Address,DOB,Gender,Contact,Photo)values(?,?,?,?,?,?,?,?,?)"); /* inserts into the database */

                pst.setString(1, Customer_ID); /* converts the values as a String and inputs them in an order in the database (the number is the order for the insert and the variable is what will be inserted into the database) */
                pst.setString(2, First_Name);
                pst.setString(3, Last_Name);
                pst.setString(4, Passport_ID);
                pst.setString(5, Address);
                pst.setString(6, date);
                pst.setString(7, Gender);
                pst.setString(8, Contact);
                pst.setBytes(9, userimage); /* inserts as Bytes the userimage for the customer into the database */

                pst.executeUpdate(); /* database update */

                JOptionPane.showMessageDialog(null, "Registration created..."); /* confirmation message for the data insertion in the database */

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Add_Customer.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Add_Customer.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed
   
    public void autoID(){
        try {
            Class.forName("com.mysql.jdbc.Driver"); /* it is a method that returns the class object associated with the class or interface passed as the first parameter */
            con = DriverManager.getConnection("jdbc:mysql://localhost/airplane_ticket_reservation","root",""); /* establishes connection to the local host */
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery("select MAX(id) from customer"); /* searches the max ID in the database for the customer */
            rs.next();
            rs.getString("MAX(id)");
            if (rs.getString("MAX(id)") == null){
                Customer_ID_Add_Customer.setText("CS001");
            }
            else{
                long id = Long.parseLong(rs.getString("MAX(id)").substring(2,rs.getString("MAX(id)").length()));
                id++;
                Customer_ID_Add_Customer.setText("CS" + String.format("%03d", id));                
            } 
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Add_Customer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Add_Customer.class.getName()).log(Level.SEVERE, null, ex);
        }        
    }

